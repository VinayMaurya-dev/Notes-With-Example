//4. Write a program to check whether the given number is positive number or not. 
#include<stdio.h>
void main()
{
	int num;
	printf("Enter a number :\n");
	scanf("%d",&num);
	if(num>0)
	{
		printf("Yes ! this number is Positive");
	}
	else
	{
		
		printf("Sorry ! this number is  Negative  ");
	}
}

